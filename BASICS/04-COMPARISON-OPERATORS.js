// num == 8 will return false 

// The comparison operators are.

// (==) ~ Equal to

// (===) ~ Identical 

// (!=)  ~ Not Equal To

// (!==) ~ Not Identical 

// (>)   ~ Greater than

// (>=)  ~ Less Than

// (<=)  ~ Less than or equal to
